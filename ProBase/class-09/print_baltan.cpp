#include<stdio.h>

const char* baltan[2][3][7] = {
	{
		{ " ", "Y", "o", "\\", "o", "Y", " " },
		{ " ", " ", " ", "w", " ", " ", " " },
		{ " ", " ", "|", " ", "|", " ", " " }
	},
	{
		{ " ", " ", "o", "\\", "o", " ", " " },
		{ ">", "-", " ", "w", " ", "-", "<" },
		{ " ", " ", "<", " ", ">", " ", " " }
	}
};

int main() {
	for (int n = 0; n < 2; n++) {
		for (int y = 0; y < 3; y++) {
			for (int x = 0; x < 7; x++) {
				printf("\x1b[%d;%dH%s", 3 * n + y + 1, x + 1, baltan[n][y][x]);
			}
		}
	}

	return 0;
}
